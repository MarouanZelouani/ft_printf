#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*node;

	node = NULL;
	while (lst)
	{
		if (lst->next == NULL)
			node = lst;
		lst = lst->next;
	}
	return (node);
}
